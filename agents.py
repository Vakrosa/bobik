"""Команда ИИ-агентов: роли общаются между собой и дают итог.
Все агенты работают через gcp_media.chat (Gemini/Grok на Vertex). Отдельных ботов нет —
один бот играет все роли и постит их реплики в чат."""
import gcp_media

FLASH = "google/gemini-2.5-flash"
PRO = "google/gemini-2.5-pro"
GROK = "xai/grok-4.3"

# key: (имя в чате, модель, характер/обязанность)
ROLES = {
    "director":   ("🎬 Режиссёр",   PRO,   "Ты режиссёр и креативный лидер. Отвечаешь за общую идею, концепцию и эмоцию. Мысли смело."),
    "prompter":   ("✍️ Промптер",   PRO,
                   "Ты ведущий эксперт по промптам для генерации видео (Veo) и изображений. "
                   "Пишешь ПОЛНЫЕ детальные промпты на английском, расписывая всё до мелочей: "
                   "субъект, действие, окружение, ракурс и движение камеры, свет, цвет, текстуры, "
                   "настроение, стиль, референсы, качество (cinematic, 8K). Никаких коротких отписок — "
                   "давай конкретный, готовый к вставке промпт под каждую сцену."),
    "editor":     ("🎞 Монтажёр",   FLASH, "Ты монтажёр. Отвечаешь за ритм, склейки, переходы, музыку, длительность сцен."),
    "analyst":    ("📊 Аналитик",   PRO,   "Ты аналитик. Цифры, логика, риски, целевая аудитория, трезвые выводы. Замечаешь слабые места."),
    "researcher": ("🔎 Ресёрчер",   GROK,  "Ты ресёрчер. Даёшь актуальные факты, тренды, примеры, ориентиры рынка. Коротко и по делу."),
    "coder":      ("💻 Кодер",      GROK,  "Ты инженер-программист. Технические и кодовые решения. Участвуешь ТОЛЬКО если задача касается кода или техники."),
    "marketer":   ("🎯 Маркетолог", PRO,   "Ты маркетолог. Позиционирование, целевая аудитория, цепляющие посылы, каналы продвижения, конверсия."),
    "financier":  ("💰 Финансист",  PRO,   "Ты финансист. Бюджет, окупаемость, риски затрат. Подсказываешь, где сэкономить, а где вложиться."),
}

ALL_KEYS = list(ROLES.keys())


def _ask(model, instruction, user_text):
    history = [{"role": "user", "text": f"{instruction}\n\n{user_text}"}]
    return gcp_media.chat(history, model)


def plan(task):
    instr = (
        "Ты диспетчер команды ИИ. Есть роли с ключами:\n"
        "director (идея/концепция), prompter (промпты для медиа), editor (монтаж),\n"
        "analyst (анализ/выводы), researcher (факты/тренды), coder (код/техника — только для технических задач),\n"
        "marketer (маркетинг/продвижение), financier (бюджет/деньги).\n"
        "Выбери от 2 до 5 ролей, которые реально нужны для задачи пользователя. "
        "coder включай ТОЛЬКО если задача про код/технику. "
        "Ответь СТРОГО списком ключей через запятую, без пояснений."
    )
    try:
        raw = _ask(FLASH, instr, f"Задача: {task}")
        picked = [k.strip().lower() for k in raw.replace("\n", ",").split(",")]
        keys = [k for k in picked if k in ROLES]
    except Exception:
        keys = []
    if len(keys) < 2:
        keys = ["director", "analyst", "researcher"]
    seen, ordered = set(), []
    for k in keys:
        if k not in seen:
            seen.add(k); ordered.append(k)
    return ordered[:5]


def speak(key, task, transcript):
    name, model, persona = ROLES[key]
    disc = "\n".join(f"{n}: {t}" for n, t in transcript) or "(обсуждение только началось)"
    instr = (
        f"{persona}\n"
        "Ты участвуешь в живом обсуждении команды. Прочитай реплики коллег и добавь СВОЮ — "
        "по своей роли. Можешь соглашаться, спорить, дополнять. 2-4 предложения, по делу, без воды."
    )
    user = f"ЗАДАЧА: {task}\n\nОБСУЖДЕНИЕ:\n{disc}\n\nТвоя реплика ({name}):"
    return _ask(model, instr, user)


def finalize(task, transcript):
    disc = "\n".join(f"{n}: {t}" for n, t in transcript)
    instr = (
        "Ты ведущий команды. На основе обсуждения дай пользователю ЗАКОНЧЕННЫЙ результат, "
        "доведённый до конца — не 'дальше обсудим', а готовое решение и конкретные шаги. "
        "Если задача про видео/картинку — ОБЯЗАТЕЛЬНО приведи полные, детальные, готовые к вставке "
        "промпты (на английском) по каждой ключевой сцене. Структурно, без воды."
    )
    user = f"ЗАДАЧА: {task}\n\nОБСУЖДЕНИЕ КОМАНДЫ:\n{disc}\n\nИтоговый результат:"
    return _ask(PRO, instr, user)


def brief(task, final_text):
    """Резюмёр: сжимает итог команды до 1-2 предложений (для длинных ответов)."""
    instr = ("Ты резюмёр. Сократи ответ команды до 1-2 КОРОТКИХ предложений — "
             "только суть и что делать. Простым языком, без воды и списков.")
    return _ask(FLASH, instr, f"ЗАДАЧА: {task}\n\nОТВЕТ КОМАНДЫ:\n{final_text}")
